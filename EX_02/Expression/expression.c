#include <stdio.h>

// main() 함수 시작 
int main(void)
{
	int a = 10, b = 5;

	printf("10 - 5 = %d\n", a - b);
	printf("10 + 5 = %d\n", a + b);
	printf("10 * 5 = %d\n", a * b);
	printf("10 / 5 = %d\n", a / b);

	return 0;
}
